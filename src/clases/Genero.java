package clases;

public class Genero {
	
	
	protected int codGen;
	protected String gen;
	
	public Genero() {
		this.codGen = -1;
	}
	public int getCodGen() {
		return codGen;
	}
	public void setCodGen(int codGen) {
		this.codGen = codGen;
	}
	public String getGen() {
		return gen;
	}
	public void setGen(String gen) {
		this.gen = gen;
	}
}
